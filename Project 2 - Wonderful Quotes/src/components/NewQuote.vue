<template>

    <div class="row">
        <form>
            <div class="col-sm-8 sol-sm-offset-2 col-xs-12 col-md-6 col-md-offset-3">
                <label>Quote</label>
                <textarea class="form-control" cols="30" rows="3" v-model="quote"></textarea>
            </div>
            <div class="col-sm-8 sol-sm-offset-2 col-xs-12 col-md-6 col-md-offset-3">
                <button class="btn btn-primary" @click.prevent="crearQuote">Crear Quote</button>
            </div>
        </form>
    </div>
    

</template>

<script>

    export default {
        data: function() {
            return {
                quote: ''
            }
        },
        methods: {
            crearQuote() {
                this.$emit('quoteAdded', this.quote);
                this.quote = '';
            }
        }
    }
</script>

<style scoped>
    div {
        border: green 2px;
    }
</style>
